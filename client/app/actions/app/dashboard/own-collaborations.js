export const OWN_COLLABORATIONS_START_REQUESTS = 'OWN_COLLABORATIONS_START_REQUESTS';
export const OWN_COLLABORATIONS_REQUESTS_FAILURE = 'OWN_COLLABORATIONS_REQUESTS_FAILURE';
export const OWN_COLLABORATIONS_REQUESTS_SUCCESS = 'OWN_COLLABORATIONS_REQUESTS_SUCCESS';


export function startOwnCollaborationsRequests(userId) {
  return {
    type: OWN_COLLABORATIONS_START_REQUESTS,
    payload: userId,
  };
}
